$(document).ready(function () {
  // The default language is English

  mainlogic();
});

function langdropdown() {
  var x = document.getElementById("LangSelect").value;
  setLanguage(x);
}
var lang;
function getLanguage() {
  if (localStorage.getItem("lang") == null) {
    setLanguage("en");
    lang = "en";
  } else {
    setLanguage(localStorage.getItem("lang"));
  }
}

function mainlogic() {
  getLanguage();
  $(".lang").each(function (index, element) {
    $(this).text(arrLang[lang][$(this).attr("key")]);
  });
  $(".inputs").each(function (index, element) {
    $(this).attr("placeholder", arrLang[lang][$(this).attr("key")]);
  });

  $(".btnlang").each(function (index, element) {
    $(this).prop("value", arrLang[lang][$(this).attr("key")]);
  });
}

function setLanguage(langx) {
  localStorage.setItem("lang", langx);
  lang = langx;

  $("#langhidden").val(langx);

  $(".lang").each(function (index, element) {
    $(this).text(arrLang[lang][$(this).attr("key")]);
  });
  $(".inputs").each(function (index, element) {
    $(this).attr("placeholder", arrLang[lang][$(this).attr("key")]);
  });

  $(".btnlang").each(function (index, element) {
    $(this).prop("value", arrLang[lang][$(this).attr("key")]);
  });

  if (lang == "en") {
    $("#li_eng").css("display", "none");
    $("#li_hin").css("display", "block");
    $(".resize").css("font-size", "16px");
    $(".lang-en").css("display", "block");
    $(".lang-hi").css("display", "none");
    $("#LangSelect").val("en");
    // $('.dynamic_link').attr("href", "About.aspx");   /* To bind url dinamically */
  } else {
    $("#li_hin").css("display", "none");
    $("#li_eng").css("display", "block");
    $(".resize").css("font-size", "16px");
    $(".lang-en").css("display", "none");
    $(".lang-hi").css("display", "block");
    $("#LangSelect").val("hi");
    // $('.dynamic_link').attr("href", "About_hi.aspx");
  }

  $(".main-menu li").each(function (index, element) {
    if ($(this).children("div").length > 0) {
      $(this).find("> a").append('<span class="indicator">+</span>');

      $(this)
        .children("a")
        .click(function (e) {
          e.preventDefault();
        });
    }
  });
}
